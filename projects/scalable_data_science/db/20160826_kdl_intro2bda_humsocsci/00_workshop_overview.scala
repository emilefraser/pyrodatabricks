// MAGIC ### Overview
// MAGIC 
// MAGIC This workshop will introduce elements of Scalable Data Science for humanities and social science researchers using Apache Spark over a Databricks shard. It will guide attendees through hands-on analysis of US State of the Union addresses, Wikipedia click-streams, live Tweets, and the Old Bailey Online dataset.
// MAGIC 
// MAGIC ### What we'll do
// MAGIC 
// MAGIC The workshop will introduce the basics of the in-memory distributed computing framework Apache Spark, including basic map-reduce operations via Spark's resilient distributed datasets (RDDs) for a word-count of US State of the Union addresses (first 40 minutes), data exploration via no-sql queries using Spark's dataframes for Wiki click-streams (30-40 minutes), Spark-streaming for filtering and getting top hash-tags of live tweets (30-40 minutes) and finally the loading, xml-parsing and the beginnings of exploration of the Old Bailey Online dataset (40 minutes, including discussions). There will be a 20 minute break during the workshop.
// MAGIC 
// MAGIC ### Who should attend?
// MAGIC 
// MAGIC Researchers in the humanities and social sciences who would like an introduction to big data analysis, using industry-standard tools. The workshop will be technical, and best suited to people with a good grasp of programming. More advanced users will be able to extend themselves. Non-programmers interested in seeing 'under the hood' of data analysis, perhaps in order to collaborate more effectively with technical colleagues, are also welcome.
